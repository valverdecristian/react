// const vs let
const firstName = 'Cristian';
const lastName = 'Valverde';

// al usar let se muestra el tipo de dato, ya que puede cambiar.
let number = 5;
number = 3;

console.log(firstName, lastName, number);

// al usar los corchetes en un clog se muestra la variable y su valor.
console.log({firstName, lastName});
